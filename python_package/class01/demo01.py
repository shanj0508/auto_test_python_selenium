# 注释
'''
注释包括：
    单行注释：#
    多行注释：三个单引号  或  三个双引号
'''

# 变量：存储数据
# 格式：变量名 = 值   等号是赋值
a = 3
print(a)

# 导入模块
'''
导入模块有三种方式：
    方式一：
        导入：import 模块名    
        使用：模块名.函数名
    方式二：
        导入：from 模块名 import 函数名1,函数名2,...,函数名n
        使用：直接用函数名
    方式三：
        导入：from 模块名 import *    一次性导入模块下全部函数
        使用：直接用函数名
'''
